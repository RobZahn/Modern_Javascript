// DOM Intro

// The Document object is the entry point into the DOM. It contains representations of all the content on a page, plus tons of useful methods and properties. Objects within the document are assembled into a tree.

// Ex.

<body>
  <h1>Hello!</h1>
  <ul>
    <li>Water Plants</li>
    <li>Get Some Sleep</li>
  </ul>
</body>;

