// ARROW FUNCTIONS

// Alternative and compact way of defining a function expression.

// Regular Expression

const square = function (x) {
  return x * x;
};

// Arrow

const square2 = (x) => {
  return x * x;
};

// Regular Expression

const sum = function (x, y) {
  return x + y;
};

// Arrow

const sum2 = (x, y) => {
  return x + y;
};

// Regular Expression

const isEven = function (num) {
  return num % 2 === 0;
};

// Arrow

const isEven2 = (num) => {
  return num % 2 === 0;
};

